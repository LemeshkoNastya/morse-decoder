const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let answer = '',
        str = '',
        key = '';

    for (let i = 0; i < expr.length; i += 10) {
        str = expr.slice(i, i + 10);

        if (str === '**********') {
            answer += ' ';
        } else {
            for (let j = 0; j < 10; j += 2) {
                if (str[j] === '1') {
                    if (str[j + 1] === '0') {
                        key += '.';
                    } else {
                        key += '-';
                    }
                }
            }
            answer += MORSE_TABLE[key];
            key = '';
        }
    }
    return answer;
}

module.exports = {
    decode
}